-----------------------------------------------
--Table creation script for CONTINENTS
------------------------------------------------
DROP TABLE IF EXISTS CONTINENTS;
CREATE TABLE CONTINENTS (
  ID INT(2) NOT NULL AUTO_INCREMENT COMMENT 'Primary Key',
  CONTINENT_CODE VARCHAR(10) NOT NULL COMMENT 'Continent Code',
  CONTINENT_NAME VARCHAR(100) NOT NULL COMMENT 'Continent Name',
  PRIMARY KEY (ID),
  UNIQUE INDEX CONTINENTS_UK_1 (CONTINENT_CODE,CONTINENT_NAME)  
)
ENGINE = INNODB
CHARACTER SET utf8
COLLATE utf8_general_ci
COMMENT = 'World Continent\'s information';

